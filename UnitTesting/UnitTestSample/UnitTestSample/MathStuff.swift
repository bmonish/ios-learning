import Foundation

class MathStuff {
    
    func addNumbers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func subNumbers(x: Int, y: Int) -> Int {
        return x - y
    }
    
    func mulNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
}
